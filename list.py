#list like a array

fruits = ["Apple", "Watermelon", "Grape"]

# print list
print(fruits)

# print spesific list
print(fruits[1])

# append to list
fruits.append("Mangos")
print(fruits)

# insert into position
fruits.insert(1, "Orange")
print(fruits)

# remove item list
fruits.remove('Orange')
print(fruits)

# pop to list
fruits.pop(0)
print(fruits)

# reverse list
fruits.reverse()
print(fruits)

# sort list
fruits.sort()
print(fruits)